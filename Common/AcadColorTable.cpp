COLORREF GetRGBColor( long nColorIndex )
{
	if( nColorIndex == -22 )
	{ //model space background
	#ifdef _ACADTARGET
		AcColorSettings stColors;
		if( !acedGetCurrentColors( &stColors ) )
			return RGB(0,0,0);
		return stColors.dwGfxModelBkColor;
	#else //_ACADTARGET
		return RGB(0,0,0);
	#endif //_ACADTARGET
	}

	if( nColorIndex == -23 )
	{ //paper space background
	#ifdef _ACADTARGET
		AcColorSettings stColors;
		if( !acedGetCurrentColors( &stColors ) )
			return RGB(0,0,0);
		return stColors.dwGfxLayoutBkColor;
	#else //_ACADTARGET
		return RGB(255,255,255);
	#endif //_ACADTARGET
	}

	if( nColorIndex == 256 )
	{
	#ifdef _ACADTARGET
		CAutoDocReadLock CurDocLock;
		AcDbObjectId idLayer = acdbHostApplicationServices()->workingDatabase()->clayer();
		AcDbLayerTableRecord* pLayer = NULL;
		if( Acad::eOk != acdbOpenObject( pLayer, idLayer, AcDb::kForRead ) )
			return RGB(255,255,255);
		nColorIndex = pLayer->color().colorIndex();
		pLayer->close();
		return RGB(acadcol[nColorIndex][0], acadcol[nColorIndex][1], acadcol[nColorIndex][2]);
	#else //_ACADTARGET
		return RGB(255,255,255);
	#endif //_ACADTARGET
	}	

	if( nColorIndex >= 0 && nColorIndex <= 255 )
		return RGB(acadcol[nColorIndex][0], acadcol[nColorIndex][1], acadcol[nColorIndex][2]);

	if( nColorIndex < 0 )
		return GetSysColor( -nColorIndex - 1 );

	return (COLORREF)nColorIndex;
}
